a = float(input("학점 : "))

if a == 4.5:
    print("신")
elif a >= 4.2:
    print("교수님의 사랑")
elif a >= 3.5:
    print("현 체제의 수호자")
elif a >= 2.8:
    print("일반인")
elif a >= 2.3:
    print("일탈을 꿈꾸는 소시민")
elif a >= 1.75:
    print("불가촉천민")
elif a >= 1.0:
    print("자벌레")
elif a >= 0.5:
    print("플랑크톤")
else:
    print("시대를 앞서가는 혁명의 씨앗")