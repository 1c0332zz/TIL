package chap_02;

public class _03_Operator3 {
    // 비교 연산자
    public static void main(String[] args) {
        System.out.println(5 > 3);
        System.out.println(5 < 3);
        System.out.println(5 >= 3);
        System.out.println(5 <= 3);
        System.out.println(5 == 3);
        System.out.println(5 != 3);
    }
}
